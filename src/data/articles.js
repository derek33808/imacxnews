// Runtime articles array that can be modified
let articles = [...initialArticlesData];

// Data versioning to refresh localStorage when seed data changes
const DATA_VERSION = 2; // bump this to force refresh

// Initialize articles from localStorage or use initial data
function initializeArticles() {
  if (typeof window !== 'undefined') {
    try {
      const storedVersion = Number(localStorage.getItem('imacx_data_version') || '0');
      const storedArticles = localStorage.getItem('imacx_articles');
      if (storedArticles) {
        const parsedArticles = JSON.parse(storedArticles);
        if (storedVersion !== DATA_VERSION) {
          // Seed changed → refresh storage with initial data
          articles = [...initialArticlesData];
          localStorage.setItem('imacx_articles', JSON.stringify(articles));
          localStorage.setItem('imacx_data_version', String(DATA_VERSION));
          console.log('Refreshed localStorage with new seed data version:', DATA_VERSION);
        } else if (Array.isArray(parsedArticles) && parsedArticles.length > 0) {
          articles = parsedArticles;
          console.log('Loaded articles from localStorage:', articles.length, articles.map(a => a.title));
        } else {
          articles = [...initialArticlesData];
          localStorage.setItem('imacx_articles', JSON.stringify(articles));
          localStorage.setItem('imacx_data_version', String(DATA_VERSION));
          console.log('Using initial articles data:', articles.length, articles.map(a => a.title));
        }
      } else {
        articles = [...initialArticlesData];
        localStorage.setItem('imacx_articles', JSON.stringify(articles));
        localStorage.setItem('imacx_data_version', String(DATA_VERSION));
        console.log('Initialized with initial articles data:', articles.length, articles.map(a => a.title));
      }
    } catch (error) {
      console.error('Error loading articles from storage:', error);
      articles = [...initialArticlesData];
      if (typeof localStorage !== 'undefined') {
        localStorage.setItem('imacx_articles', JSON.stringify(articles));
        localStorage.setItem('imacx_data_version', String(DATA_VERSION));
      }
    }
  }
  return articles;
}

// Initialize on module load
if (typeof window !== 'undefined') {
  initializeArticles();
}

export { initialArticlesData };

export function getArticleBySlug(slug) {
  initializeArticles();
  return articles.find(article => article.slug === slug) || null;
}

export function getArticlesByCategory(categoryId) {
  initializeArticles();
  return articles.filter(article => article.category === categoryId);
}

export function getFeaturedArticles() {
  initializeArticles();
  return articles.filter(article => article.featured);
}

export function getLatestArticles(count = 6) {
  initializeArticles();
  return [...articles]
    .sort((a, b) => new Date(b.publishDate) - new Date(a.publishDate))
    .slice(0, count);
}

export function getRelatedArticles(currentArticleId, count = 3) {
  initializeArticles();
  const currentArticle = articles.find(article => article.id === currentArticleId);
  if (!currentArticle) return [];
  
  return articles
    .filter(article => article.id !== currentArticleId && article.category === currentArticle.category)
    .sort(() => 0.5 - Math.random())
    .slice(0, count);
}

export function addArticle(newArticle) {
  try {
    initializeArticles();
    const id = articles.length > 0 ? Math.max(...articles.map(a => a.id)) + 1 : 1;
    const slug = newArticle.title.toLowerCase()
      .replace(/[^a-z0-9\s-]/g, '')
      .replace(/\s+/g, '-')
      .replace(/-+/g, '-')
      .trim();
    
    const article = {
      id,
      slug,
      publishDate: new Date().toISOString(),
      featured: false,
      ...newArticle
    };
    
    articles.unshift(article);
    
    // Store articles in localStorage for persistence
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem('imacx_articles', JSON.stringify(articles));
    }
    
    return article;
  } catch (error) {
    console.error('Error adding article:', error);
    return null;
  }
}

export function updateArticle(id, updatedData) {
  try {
    initializeArticles();
    const index = articles.findIndex(article => article.id === id);
    if (index === -1) {
      throw new Error('Article not found');
    }
    
    // Update slug if title changed
    if (updatedData.title && updatedData.title !== articles[index].title) {
      updatedData.slug = updatedData.title.toLowerCase()
        .replace(/[^a-z0-9\s-]/g, '')
        .replace(/\s+/g, '-')
        .replace(/-+/g, '-')
        .trim();
    }
    
    articles[index] = { ...articles[index], ...updatedData };
    
    // Store articles in localStorage for persistence
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem('imacx_articles', JSON.stringify(articles));
    }
    
    return articles[index];
  } catch (error) {
    console.error('Error updating article:', error);
    return null;
  }
}

export function deleteArticle(id) {
  try {
    initializeArticles();
    const index = articles.findIndex(article => article.id === id);
    if (index === -1) {
      throw new Error('Article not found');
    }
    
    const deletedArticle = articles[index];
    articles.splice(index, 1);
    
    // Store articles in localStorage for persistence
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem('imacx_articles', JSON.stringify(articles));
    }
    
    return deletedArticle;
  } catch (error) {
    console.error('Error deleting article:', error);
    return null;
  }
}

export function getArticleById(id) {
  initializeArticles();
  return articles.find(article => article.id === id) || null;
}

export function getAllArticles() {
  initializeArticles();
  return articles;
}

// Reload articles from storage
export function reloadArticles() {
  initializeArticles();
  return articles;
}

